#include "3-calc.h"
/**
 * op_add - addition
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - subtraction
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - product
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - division
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulo
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_mod(int a, int b)
{
return (a % b);
}
