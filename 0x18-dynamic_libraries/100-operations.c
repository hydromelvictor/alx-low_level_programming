#include <stdio.h>

/**
 * add - addition
 *
 * @a : parameter
 * @b : parameter
 * Return: int
 */
int add(int a, int b)
{
    return (a + b);
}

/**
 * sub - substraction
 *
 * @a : parameter
 * @b : parameter
 * Return: int
 */
int sub(int a, int b)
{
    return (a - b);
}

/**
 * mul - multiplication
 *
 * @a : parameter
 * @b : parameter
 * Return: int
 */
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - division
 *
 * @a : parameter
 * @b : parameter
 * Return: int
 */
int div(int a, int b)
{
    return (a / b);
}

/**
 * mod - modulo
 *
 * @a : parameter
 * @b : parameter
 * Return: int
 */
int mod (int a, int b)
{
    return  (a % b);
}
